package org.javacream.training.java;

public class Address {

	private String city;
	private String street;
	public Address(String city, String street) {
		super();
		this.city = city;
		this.street = street;
	}
	public String getStreet() {
		return street;
	}
	public void setStreet(String street) {
		this.street = street;
	}
	public String getCity() {
		return city;
	}
	
	

}
